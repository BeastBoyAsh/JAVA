class Pattern9{
    public static void main(String[] args){
    int i,j;
    for(i=1;i<=5;i=i+1)
    {
     for(j=1;j<=i;j=j+1)
     {
         if(j%2==1){
            System.out.print("1");
        }
        else
        System.out.print("0");
       }
       System.out.println();
    }

 }
}
    
